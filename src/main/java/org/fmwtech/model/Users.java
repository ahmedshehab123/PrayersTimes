package org.fmwtech.model;

/**
 *
 * @author ahmed
 */
public class Users {
    private int id;
    private String email;
    private String password;
    private String mosqueName;
    private String mosqueLocation;

    public String getMosqueName() {
        return mosqueName;
    }

    public void setMosqueName(String mosqueName) {
        this.mosqueName = mosqueName;
    }

    public String getMosqueLocation() {
        return mosqueLocation;
    }

    public void setMosqueLocation(String mosqueLocation) {
        this.mosqueLocation = mosqueLocation;
    }
    

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
    
    
}
